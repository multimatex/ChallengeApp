package com.rapidticket.platform.domain.model;

public class Section {
    private long id;
    private String name;

    public Section() {
    }

    public Section(long id, String name) {
        this.id = id;
        this.name = name;
    }

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
